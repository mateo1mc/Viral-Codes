import turtle

turtle.title('Adidas Logo')
turtle.setup(1000, 1000)
turtle.setworldcoordinates(-200, -150, 200, 250)
turtle.tracer(0, 0)
turtle.begin_fill()
turtle.circle(100, 90, 200)
turtle.right(270)
turtle.circle(100, 90, 200)
turtle.end_fill()

turtle.up()
turtle.goto(-10, 0)
turtle.down()
turtle.seth(45)
turtle.begin_fill()
turtle.circle(100, 90, 200)
turtle.right(270)
turtle.circle(100, 90, 200)
turtle.end_fill()

turtle.up()
turtle.goto(-20, 0)
turtle.down()
turtle.seth(90)
turtle.begin_fill()
turtle.circle(100, 90, 200)
turtle.right(270)
turtle.circle(100, 90, 200)
turtle.end_fill()
turtle.pensize(20)

turtle.up()
turtle.color("white")
turtle.goto(-200, 56)
turtle.down()
turtle.fd(400)
turtle.pensize(20)

turtle.up()
turtle.color("white")
turtle.goto(-200, 40)
turtle.down()
turtle.fd(400)
turtle.pensize(20)

turtle.up()
turtle.color("white")
turtle.goto(-200, 24)
turtle.down()
turtle.fd(400)

turtle.mainloop()
